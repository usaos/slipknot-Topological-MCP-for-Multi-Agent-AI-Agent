"""
量化交易行业插件
面向量化投研Agent的拓扑诊断提示
输出交易策略调整建议
"""


def get_quant_tip(h1_count: int, h0_count: int = 0) -> str:
    """
    根据拓扑分析结果生成量化交易策略提示
    :param h1_count: H1一维孔洞数量（市场循环结构数量）
    :param h0_count: H0连通域数量（市场聚类数量）
    :return: 交易策略提示文本
    """
    if h1_count >= 3:
        return """📈【高波动预警】市场拓扑存在多重循环结构
拓扑诊断：多周期套利循环共存，市场处于复杂纠缠态
策略操作指令：
1. 量化Agent降低单边仓位30%，转向中性套利策略
2. 启用多因子风控模型，严控单一风格暴露
3. 增加对冲比例，防范系统性回调风险
4. 缩短持仓周期，提高交易频率捕捉短周期机会
"""
    elif h1_count >= 2:
        return """📊【中等波动】市场存在双循环拓扑结构
拓扑诊断：主趋势与震荡周期交织，方向不明确
策略操作指令：
1. 降低杠杆比例，控制单笔交易风险
2. 配置震荡策略与趋势策略各50%仓位
3. 加强止损纪律，扩大止损区间10%
4. 关注成交量奇点，识别真假突破
"""
    elif h0_count >= 3:
        return """🔀【风格切换预警】市场拓扑多聚类分离
拓扑诊断：板块/风格分化严重，资金分流明显
策略操作指令：
1. 避免全市场统一策略，分板块独立建模
2. 关注资金流向奇点，捕捉风格切换机会
3. 降低行业集中度，分散配置降低波动
"""
    else:
        return """✅【平稳市场】市场拓扑结构单一
拓扑诊断：市场趋势清晰，无明显循环纠缠
策略操作指令：维持现有策略运行，正常仓位交易
"""
